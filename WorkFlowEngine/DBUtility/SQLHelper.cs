        /// <summary>
        /// Execute multiple sql statements as a transaction
        /// </summary>
        /// <param name="SQLStringArray">sql statement arrays</param>
        /// <returns>return true if succeeds and false if not</returns>
        public static bool ExecuteSqlByTransaction(ArrayList SQLStringArray)
        {
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                using (SqlCommand cmd = connection.CreateCommand())
                {
                    connection.Open();

                    SqlTransaction sqlTransaction = connection.BeginTransaction();
                    cmd.Transaction = sqlTransaction;

                    try
                    {
                        for (int i = 0; i < SQLStringArray.Count; i++)
                        {
                            cmd.CommandText = SQLStringArray[i].ToString();
                            cmd.ExecuteNonQuery();
                        }

                        sqlTransaction.Commit();
                        return true;
                    }
                    catch (SqlException ex)
                    {
                        sqlTransaction.Rollback();
                        throw ex;
                    }
                }
            }
        }

